package lista05_1;

//Desenvolver um programa que exiba os números de 1 a 100.

public class Questao01 {

    public static void main(String[] args) {
	int cont;
	cont = 1;
	while (cont <= 100) {
	    System.out.println(cont + ".");
	    cont++;
	}
	System.out.println("Aqui estão os números de 1 a 100.");
    }

}
